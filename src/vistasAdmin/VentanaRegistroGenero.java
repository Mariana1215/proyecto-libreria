    private void llenarTabla(){
        DefaultTableModel modelo = new DefaultTableModel();
        
        ArrayList<Object> generos = controlador.listarObjeto();
        
        modelo.setColumnIdentifiers(new Object[]{
            "ID genero", "Nombre genero"
        });
        tablaGeneros.setModel(modelo);
        tablaGeneros.setAutoCreateRowSorter(true);
        sorter = new TableRowSorter<>(modelo);
        tablaGeneros.setRowSorter(sorter);
        
        for (int i = 0; i < generos.size(); i++) {
           Genero genero = (Genero) generos.get(i);
            modelo.addRow(new Object[]{
                genero.getIdGenero(),
                genero.getNombreGenero()
            });
        }
        
    }
    
    private void filtrar(){
        String filterText = txtBuscarGenero.getText().trim();
        
        RowFilter<Object, Object> idGenero = RowFilter.regexFilter(filterText.trim(), 0);
        RowFilter<Object, Object> nombreGenero = RowFilter.regexFilter(filterText.trim(), 1);
        
        sorter.setRowFilter(RowFilter.orFilter(Arrays.asList(idGenero, nombreGenero)));
    }
    private boolean validarCampos(){
        return (txtNombreGenero.getText().isEmpty());
    }
    private void limpiarCampos() {
        txtNombreGenero.setText("");
        
    }
