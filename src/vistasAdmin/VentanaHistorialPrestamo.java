    private void llenarTabla(){
        DefaultTableModel modelo = new DefaultTableModel();
        
        ArrayList<Prestamo> prestamos = controlador.listarTodosPrestamos();
        modelo.setColumnIdentifiers(new Object[]{
            "Cedula usuario", "Usuario", "ISBN", "TÃ­tulo", "Cantidad", "Fecha prestamo", "Fecha Vencimiento"
        });
        tablaHistorial.setModel(modelo);
        
        for(Prestamo prestamo : prestamos){
            modelo.addRow(new Object[]{
                prestamo.getUsuario().getCedula(),
                prestamo.getUsuario().getNombre(),
                prestamo.getLibro().getISBN(),
                prestamo.getLibro().getTitulo(),
                prestamo.getCantidadLibro(),
                prestamo.getFechaPrestamo(),
                prestamo.getFechaLimite()
            });
        }
        
        
    }

