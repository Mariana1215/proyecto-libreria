    private void btnRealizarDevolucionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRealizarDevolucionActionPerformed
        // TODO add your handling code here:
        int selec = tablaPrestamo.getSelectedRow();

        if (selec >= 0) {
            int idPrestamo = (int) tablaPrestamo.getModel().getValueAt(selec, 0);
            Prestamo prestamo = controlador.seleccionarPrestamo(idPrestamo);
            controlador.delvoverPrestamo(prestamo);
            llenarTabla();
            JOptionPane.showMessageDialog(null, "Devolucion realizada");

        } else {
            JOptionPane.showMessageDialog(null, "Sleecionar un prestamo de la tabla");
        }

    }//GEN-LAST:event_btnRealizarDevolucionActionPerformed

    private void llenarTabla(){
        DefaultTableModel modelo = new DefaultTableModel();
        
        ArrayList<Prestamo> prestamos = controlador.listarUsuarioPrestamo(usuario);
        modelo.setColumnIdentifiers(new Object[]{
            "Id prestamo","ISBN", "TÃ­tulo","Cantidad", "Fecha prestamo", "Fecha vencimiento"
        });
        tablaPrestamo.setModel(modelo);
        tablaPrestamo.setAutoCreateRowSorter(true);
        sorter = new TableRowSorter<>(modelo);
        tablaPrestamo.setRowSorter(sorter);
        
        for (Prestamo prestamo : prestamos){
            modelo.addRow(new Object[]{
                prestamo.getId(),
                prestamo.getLibro().getISBN(),
                prestamo.getLibro().getTitulo(),
                prestamo.getCantidadLibro(),
                prestamo.getFechaPrestamo(),
                prestamo.getFechaLimite()
            });
        }
    }
