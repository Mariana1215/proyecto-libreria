    private void mostrarInformacionLibro(){
        lblInfoTitulo.setText(libro.getTitulo());
        lblInfoIsbn.setText(libro.getISBN());
        lblInfoAutor.setText(libro.getAutor());
        cbxAnioPublicacion.setSelectedItem(libro.getAnio());
        cbxGenero.setSelectedItem(libro.getGenero());
    }
    
    private void setCbxAnioPublicacion(){
        int fechaActual = LocalDate.now().getYear();
        
        for(int anio = fechaActual; anio >= (fechaActual - 400); anio--){
            cbxAnioPublicacion.addItem(String.valueOf(anio));
        }
        cbxAnioPublicacion.insertItemAt("Seleccione un opción", 0);
    }
    private void setCbxGenero(){
        DefaultComboBoxModel<String>  modelo = new DefaultComboBoxModel<>();
        cbxGenero.setModel(modelo);
        
        ArrayList<Genero> generos = controladorLibro.getGeneros();
        modelo.addElement("seleccione un genero");
        
        for(Genero genero : generos){
            modelo.addElement(genero.getNombreGenero());
        }
    }

