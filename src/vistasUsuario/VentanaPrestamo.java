    private void btnRealizarPrestamoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRealizarPrestamoActionPerformed
        // TODO add your handling code here:
        Date fecha = fechaDevolucion.getDate();
        
        if (fecha == null || txtCantidad.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingrese todos los datos");
            return;
        }
        int cantidadLibros = Integer.parseInt(txtCantidad.getText());
        LocalDate fechaPrestamo = LocalDate.now();
        LocalDate fechaDevolucion = fecha.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        
        try{
            Prestamo prestamo = new Prestamo(usuario, libro, fechaPrestamo, cantidadLibros, fechaDevolucion);
            controladorPrestamo.solicitarPrestamo(prestamo);
            JOptionPane.showMessageDialog(null, "Prestamo realizado con éxito");
            this.dispose();
        }catch (PrestamoVencidoException | CantidadException ex){
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnRealizarPrestamoActionPerformed

