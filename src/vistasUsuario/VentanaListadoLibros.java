    private void btnRealizarPrestamoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRealizarPrestamoActionPerformed
        // TODO add your handling code here:
        int selec = tablaLibros.getSelectedRow();
        
        if(selec >= 0){
            String isbn = tablaLibros.getModel().getValueAt(selec, 0).toString();
            Libro libro = (Libro) controlador.buscarObjeto(isbn);
            
            if (libro != null){
                new VentanaPrestamo(usuario, libro).setVisible(true);
                
            }
        }else{
            JOptionPane.showMessageDialog(null, "Debe de seleccionar un libro de la tabla");
        }
    }//GEN-LAST:event_btnRealizarPrestamoActionPerformed

