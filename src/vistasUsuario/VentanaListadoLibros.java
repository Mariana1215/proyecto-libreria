    private final void llenarTabla(){
        DefaultTableModel modelo = new DefaultTableModel();
        
        ArrayList<Object> libros = controlador.listarObject();
        modelo.setColumnIdentifiers(new Object[]{
            "ISBN", "Titulo", "Autor", "Genero", "AÃ±o publicacion"
        });
        
        tablaLibros.setModel(modelo);
        tablaLibros.setAutoCreateRowSorter(true);
        sorter = new TableRowSorter<>(modelo);
        tablaLibros.setRowSorter(sorter);
        
        for (int i = 0; i < libros.size(); i++) {
            Libro libro = (Libro) libros.get(i);
            modelo.addRow(new Object[]{
                libro.getISBN(),
                libro.getTitulo(),
                libro.getAutor(),
                libro.getGenero().getNombreGenero(),
                libro.getAnio(),
            });
        }
        
    }
    
    private void filtro(){
        String filtro = txtBuscar.getText();
        
        RowFilter<Object, Object>  isbn = RowFilter.regexFilter(filtro.trim(), 0);
        RowFilter<Object, Object>  titulo = RowFilter.regexFilter(filtro.trim(), 1);
        RowFilter<Object, Object>  autor = RowFilter.regexFilter(filtro.trim(), 3);
        RowFilter<Object, Object>  genero = RowFilter.regexFilter(filtro.trim(), 4);
        RowFilter<Object, Object>  anioPublicacion = RowFilter.regexFilter(filtro.trim(), 5);
        RowFilter<Object, Object>  copias = RowFilter.regexFilter(filtro.trim(), 6);
        
        sorter.setRowFilter(RowFilter.orFilter(Arrays.asList(isbn, titulo, autor, genero, anioPublicacion, copias)));

        
    }

